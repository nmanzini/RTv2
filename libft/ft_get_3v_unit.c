#include "libft.h"

t_3v	ft_get_3v_unit(int dir)
{
	t_3v	vector;
	int		i;

	i = 0;
	while (i < 3)
	{
		if (i == dir)
			(vector.v)[i] = 1.0;
		else
			(vector.v)[i] = 0;
		i++;
	}
	return (vector);
}

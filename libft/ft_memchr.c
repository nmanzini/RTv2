#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*s2;

	s2 = (char *)s;
	while (n)
	{
		if (*s2 == (char)c)
			return ((void *)s);
		s2++;
		s++;
		n--;
	}
	return (NULL);
}

#include "libft.h"

int	ft_isprime(int nb)
{
	int i;

	i = 3;
	if (nb == 2 || nb == 3)
		return (1);
	if (nb % 2 == 0 || nb <= 1)
		return (0);
	while ((i * i < nb) && (nb % i != 0) && (i <= 46340))
		i += 2;
	if (nb % i == 0)
		return (0);
	else
		return (1);
}

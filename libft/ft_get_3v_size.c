#include "libft.h"

double	ft_get_3v_size(t_3v v)
{
	double	size;
	int		i;

	size = 0;
	i = 0;
	while (i < 3)
	{
		size += (v.v)[i] * (v.v)[i];
		i++;
	}
	size = sqrt(size);
	return (size);
}

#include "libft.h"

size_t	ft_factorial(size_t nb)
{
	if (nb >= 13)
		return (0);
	if (nb > 1)
		nb = nb * ft_factorial(nb - 1);
	if (nb == 0)
		return (1);
	return (nb);
}

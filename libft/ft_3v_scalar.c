#include "libft.h"

void	ft_3v_scalar(t_3v *v, double s)
{
	int	i;

	if (!v)
		return ;
	i = 0;
	while (i < 3)
	{
		(v->v)[i] = (v->v)[i] * s;
		i++;
	}
}
